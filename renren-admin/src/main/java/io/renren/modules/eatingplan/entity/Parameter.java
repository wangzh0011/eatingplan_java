package io.renren.modules.eatingplan.entity;

public class Parameter {

    private String RMB;

    private String coin;

    private String coinContext;

    public String getRMB() {
        return RMB;
    }

    public void setRMB(String RMB) {
        this.RMB = RMB;
    }

    public String getCoin() {
        return coin;
    }

    public void setCoin(String coin) {
        this.coin = coin;
    }

    public String getCoinContext() {
        return coinContext;
    }

    public void setCoinContext(String coinContext) {
        this.coinContext = coinContext;
    }
}
